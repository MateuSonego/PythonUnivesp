# Operadores aritméticos em Python

#Soma 
x = 3 + 3
print("Resultado da Soma: " ,  x)

#Subtração 
x = 3 - 2
print("Resultado da Subtração: " , x) 

#Multiplicação 
x = 3 * 3
print("Resultado da Multiplicação: ", x)

#Divisão 
x = 9 / 2
print("Resultado da Divisão: ", x) 

#Divisão Apenas Inteiro 
x = 9 // 2
print("Resultado da Divisão(Inteiro): ", x) 

#Divisão Apenas Resto (Módulo)
x = 9 % 2
print("Resultado da Divisão(Módulo): ", x) 

#Potenciação
x = 9 ** 2
print("Resultado da Potenciação: ", x)  





